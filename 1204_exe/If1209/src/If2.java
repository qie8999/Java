
public class If2 {

	public static void main(String[] args) {
		//자바 프로젝트가 구동할때 먼저 main을 찾는다
		//찾고 순차적으로 내려감
		int num = 200;
		
		if (num < 100) {
			System.out.println("num가 100보다 작다.");
			System.out.println(" 2줄이상 코딩 가능 ");
		}
		else {
			System.out.println("num가 100보다 크다.");
		}
		
		
	}

}
